package ex1;
/**
 * classe permettant de calculer une addition et une soustraction
 * @author diginamic
 *
 */
public class Calcul {

	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
